    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
   protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {

    String typeName = request.getParameter("type_name");

    // Tạo đối tượng Application_Type
    ApplicationTypeDAO dao = new ApplicationTypeDAO();
    
    // Lấy ID mới tự động bằng cách sử dụng phương thức generateMaxID từ DAO
    int newId = dao.generateMaxID();

    // Tạo đối tượng ApplicationType với ID mới
    ApplicationType appType = new ApplicationType(newId, typeName);

    // Gọi DAO để thêm loại ứng dụng mới
    dao.insertApplicationType(appType);

    // Sau khi thêm xong, chuyển hướng về trang danh sách loại ứng dụng
    response.sendRedirect("ViewListApplicationType.jsp");
}

